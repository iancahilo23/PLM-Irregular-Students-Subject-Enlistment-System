CREATE PROCEDURE sp_get_registered_subjects(
    IN p_student_id     VARCHAR(50),
    IN p_semester       VARCHAR(50),
    IN p_school_year    VARCHAR(50)
)
BEGIN
    DECLARE done            INT DEFAULT 0;
    DECLARE v_subject_id    VARCHAR(50);
    DECLARE v_subject_title VARCHAR(150);
    DECLARE v_lec_hours     TIME;
    DECLARE v_lab_hours     TIME;
    DECLARE v_units         DECIMAL(4,1);
    DECLARE v_day           VARCHAR(5);
    DECLARE v_time_start    TIME;
    DECLARE v_time_end      TIME;

    -- temporary table to collect results (cursor loop will insert here)
    CREATE TEMPORARY TABLE IF NOT EXISTS tmp_registered_subjects (
        subject_id      VARCHAR(50),
        subject_title   VARCHAR(150),
        lec_hours       TIME,
        lab_hours       TIME,
        units           DECIMAL(4,1),
        day_of_week     VARCHAR(5),
        time_start      TIME,
        time_end        TIME
    );

    -- cursor selecting the required fields
    DECLARE cur_subjects CURSOR FOR
        SELECT s.subject_id,
               s.subject_title,
               s.lec_hours,
               s.lab_hours,
               s.units,
               sch.day_of_week,
               sch.time_start,
               sch.time_end
        FROM student_enlistment se
        JOIN subject s ON se.subject_id = s.subject_id AND se.section = s.section
        LEFT JOIN schedule_option so ON s.sched_option_id = so.sched_option_id
        LEFT JOIN schedule sch ON so.sched_id = sch.sched_id
        WHERE se.student_id = p_student_id
          AND se.semester = p_semester
          AND se.school_year = p_school_year
          AND se.enroll_id = 'ENR';

    DECLARE CONTINUE HANDLER FOR NOT FOUND SET done = 1;

    OPEN cur_subjects;
    read_loop: LOOP
        FETCH cur_subjects INTO v_subject_id, v_subject_title, v_lec_hours, v_lab_hours, v_units, v_day, v_time_start, v_time_end;
        IF done THEN
            LEAVE read_loop;
        END IF;
        INSERT INTO tmp_registered_subjects VALUES (v_subject_id, v_subject_title, v_lec_hours, v_lab_hours, v_units, v_day, v_time_start, v_time_end);
    END LOOP read_loop;
    CLOSE cur_subjects;

    -- return the collected rows
    SELECT * FROM tmp_registered_subjects;

    DROP TEMPORARY TABLE IF EXISTS tmp_registered_subjects;
END$$
DELIMITER ;

-- STORED PROCEDURE: Uses a CURSOR to produce subjects available to register for the student
