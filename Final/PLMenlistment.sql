DELIMITER $$

CREATE PROCEDURE sp_get_subjects_to_register(
    IN p_student_id     VARCHAR(50),
    IN p_semester       VARCHAR(50),
    IN p_school_year    VARCHAR(50)
)
BEGIN
    DECLARE done INT DEFAULT 0;
    DECLARE v_subject_id    VARCHAR(50);
    DECLARE v_subject_title VARCHAR(150);
    DECLARE v_lec_hours     TIME;
    DECLARE v_lab_hours     TIME;
    DECLARE v_units         DECIMAL(4,1);
    DECLARE v_day           VARCHAR(5);
    DECLARE v_time_start    TIME;
    DECLARE v_time_end      TIME;
    DECLARE v_enroll_title  VARCHAR(50);

    CREATE TEMPORARY TABLE IF NOT EXISTS tmp_subjects_to_register (
        subject_id      VARCHAR(50),
        subject_title   VARCHAR(150),
        lec_hours       TIME,
        lab_hours       TIME,
        units           DECIMAL(4,1),
        day_of_week     VARCHAR(5),
        time_start      TIME,
        time_end        TIME,
        enroll_title    VARCHAR(50)
    );

    -- Cursor selects subjects that the student is not already enrolled in (or only has pending enlistment)
    DECLARE cur_avail CURSOR FOR
        SELECT s.subject_id,
               s.subject_title,
               s.lec_hours,
               s.lab_hours,
               s.units,
               sch.day_of_week,
               sch.time_start,
               sch.time_end,
               IFNULL(es.enroll_title, 'Available') AS enroll_title
        FROM subject s
        LEFT JOIN schedule_option so ON s.sched_option_id = so.sched_option_id
        LEFT JOIN schedule sch ON so.sched_id = sch.sched_id
        LEFT JOIN student_enlistment se ON s.subject_id = se.subject_id
            AND s.section = se.section
            AND se.student_id = p_student_id
            AND se.semester = p_semester
            AND se.school_year = p_school_year
        LEFT JOIN enrollment_status es ON IFNULL(se.enroll_id, s.enroll_id) = es.enroll_id
        WHERE (se.enlist_id IS NULL OR se.enroll_id <> 'ENR');

    DECLARE CONTINUE HANDLER FOR NOT FOUND SET done = 1;

    OPEN cur_avail;
    read_loop: LOOP
        FETCH cur_avail INTO v_subject_id, v_subject_title, v_lec_hours, v_lab_hours, v_units, v_day, v_time_start, v_time_end, v_enroll_title;
        IF done THEN
            LEAVE read_loop;
        END IF;
        INSERT INTO tmp_subjects_to_register VALUES (v_subject_id, v_subject_title, v_lec_hours, v_lab_hours, v_units, v_day, v_time_start, v_time_end, v_enroll_title);
    END LOOP read_loop;
    CLOSE cur_avail;

    SELECT * FROM tmp_subjects_to_register;

    DROP TEMPORARY TABLE IF EXISTS tmp_subjects_to_register;
END$$
DELIMITER ;


